#!/usr/bin/env bash

set -euo pipefail

LOG_DIR="${1:-dbtestlog}"
OUT_FILE="${2:-/tmp/ycsb_comment.md}"

COMMENT_MARKER="<!-- ycsb-benchmark-comment -->"

workload_name() {
  case "$1" in
    a) echo "A (50% Read / 50% Update)" ;;
    b) echo "B (95% Read / 5% Update)" ;;
    c) echo "C (100% Read)" ;;
    d) echo "D (Read Latest)" ;;
    f) echo "F (Read-Modify-Write)" ;;
    *) echo "$1" ;;
  esac
}

extract_metric() {
  local pattern="$1"
  local file="$2"
  local line=""
  line="$(grep -E "$pattern" "$file" | tail -n1 || true)"
  [[ -n "$line" ]] || return 0
  awk -F': ' '{print $2}' <<<"$line" | tr -d '\r'
}

extract_run_summary() {
  local file="$1"
  local line=""
  line="$(grep -E "sec: [0-9]+ operations; \[" "$file" | tail -n1 || true)"
  [[ -n "$line" ]] || return 0
  sed -E 's/^.*operations;[[:space:]]*//' <<<"$line"
}

files=("$LOG_DIR"/kvdb_bench_workload*.log)
if [[ ! -e "${files[0]}" ]]; then
  cat >"$OUT_FILE" <<EOF
${COMMENT_MARKER}
## YCSB Benchmark Summary

⚠️ 未找到 YCSB 日志文件（期望路径：\`${LOG_DIR}/kvdb_bench_workload*.log\`）。
EOF
  echo "Generated: $OUT_FILE"
  exit 0
fi

TMP_TABLE="$(mktemp)"
trap 'rm -f "$TMP_TABLE"' EXIT

{
  echo "| Workload | Load Throughput (ops/s) | Run Throughput (ops/s) | Run Runtime (s) | Run Profile (interrupts/evictions/bytes) | Run Mix |"
  echo "|---|---:|---:|---:|---|---|"
} >"$TMP_TABLE"

run_tp_values=""

for file in "$LOG_DIR"/kvdb_bench_workload*.log; do
  [[ -f "$file" ]] || continue

  base="$(basename "$file")"
  letter="$(echo "$base" | sed -n 's/.*workload\([a-z]\)\.log/\1/p')"
  wl_name="$(workload_name "$letter")"

  load_tp="$(extract_metric 'Load throughput\(ops/sec\):' "$file")"
  run_tp="$(extract_metric 'Run throughput\(ops/sec\):' "$file")"
  run_rt="$(extract_metric 'Run runtime\(sec\):' "$file")"
  run_mix="$(extract_run_summary "$file")"

  run_profile="$(grep -E '^PROFILE: interrupts/evictions/bytes = ' "$file" | tail -n1 | sed -E 's/^PROFILE: interrupts\/evictions\/bytes = //' || true)"

  load_tp="${load_tp:-N/A}"
  run_tp="${run_tp:-N/A}"
  run_rt="${run_rt:-N/A}"
  run_profile="${run_profile:-N/A}"
  run_mix="${run_mix:-N/A}"

  printf '| %s | %s | %s | %s | %s | %s |\n' \
    "$wl_name" "$load_tp" "$run_tp" "$run_rt" "$run_profile" "$run_mix" >>"$TMP_TABLE"

  if [[ "$run_tp" != "N/A" ]]; then
    run_tp_values+="$run_tp "
  fi
done

if [[ -n "$run_tp_values" ]]; then
  avg_run_tp="$(awk '{s=0; for(i=1;i<=NF;i++) s+=$i; if(NF>0) printf "%.0f", s/NF; else print "N/A"}' <<<"$run_tp_values")"
  min_run_tp="$(awk 'BEGIN{m=""} {for(i=1;i<=NF;i++){if(m==""||$i<m)m=$i}} END{if(m=="")print "N/A"; else printf "%.0f", m}' <<<"$run_tp_values")"
  max_run_tp="$(awk 'BEGIN{m=""} {for(i=1;i<=NF;i++){if(m==""||$i>m)m=$i}} END{if(m=="")print "N/A"; else printf "%.0f", m}' <<<"$run_tp_values")"
else
  avg_run_tp="N/A"
  min_run_tp="N/A"
  max_run_tp="N/A"
fi

{
  echo "$COMMENT_MARKER"
  echo "## YCSB Benchmark Summary"
  echo
  printf -- '- Logs directory: `%s`\n' "$LOG_DIR"
  echo "- Run throughput (ops/s): avg=**$avg_run_tp**, min=**$min_run_tp**, max=**$max_run_tp**"
  echo
  cat "$TMP_TABLE"
  echo
  echo "> This comment is automatically generated by CI (script: ci/benchmark/ycsb.sh)."
} >"$OUT_FILE"

echo "Generated: $OUT_FILE"
